restartButton.addEventListener("click", restartQuiz);

// Fonction pour mÃ©langer un tableau (Fisher-Yates)
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// DÃ©marrage du quiz
function startQuiz() {
    // SÃ©lectionne 10 questions alÃ©atoires
    questions = shuffleArray([...uniqueQuestions]).slice(0, 10);

    // RÃ©initialise l'Ã©tat
    currentQuestionIndex = 0;
    score = 0;
    selectedChoices = [];

    // Affiche l'interface
    startScreen.classList.add("hidden");
    quizScreen.classList.remove("hidden");
    showQuestion();
}

// Afficher la question actuelle
function showQuestion() {
    const currentQuestion = questions[currentQuestionIndex];
    questionEl.textContent = `Question ${currentQuestionIndex + 1}: ${currentQuestion.question}`;
    choicesEl.innerHTML = "";

    currentQuestion.choices.forEach((choice, index) => {
        const button = document.createElement("button");
        button.textContent = choice;
        button.classList.add("choice-button");
        button.addEventListener("click", () => toggleChoice(index, button));
        choicesEl.appendChild(button);
    });

    validateButton.classList.remove("hidden");
    nextButton.classList.add("hidden");
}

// GÃ©rer la sÃ©lection des choix
function toggleChoice(index, button) {
    const currentQuestion = questions[currentQuestionIndex];

    if (currentQuestion.correct.length === 1) {
        // Une seule rÃ©ponse possible
        selectedChoices = [index];
        document.querySelectorAll("#choices button").forEach((btn) => btn.classList.remove("selected"));
        button.classList.add("selected");
    } else {
        // Plusieurs rÃ©ponses possibles
        if (selectedChoices.includes(index)) {
            selectedChoices = selectedChoices.filter((i) => i !== index);
            button.classList.remove("selected");
        } else {
            selectedChoices.push(index);
            button.classList.add("selected");
        }
    }
}

// Valider les rÃ©ponses
function validateAnswers() {
    const currentQuestion = questions[currentQuestionIndex];
    let correctCount = 0;
    let incorrectCount = 0;

    // Compter les bonnes et mauvaises rÃ©ponses
    selectedChoices.forEach((choice) => {
        if (currentQuestion.correct.includes(choice)) {
            correctCount++;
        } else {
            incorrectCount++;
        }
    });

    // Calcul du score partiel
    const maxPoints = currentQuestion.correct.length;
    const pointsEarned = (correctCount - incorrectCount) / maxPoints;
    score += Math.max(0, pointsEarned); // Ã‰viter les scores nÃ©gatifs

    // Afficher les rÃ©ponses avec les couleurs
    choicesEl.innerHTML = "";
    currentQuestion.choices.forEach((choice, index) => {
        const answerText = document.createElement("p");
        answerText.textContent = choice;

        if (currentQuestion.correct.includes(index)) {
            answerText.classList.add("correct-answer"); // Bonne rÃ©ponse
        } else {
            answerText.classList.add("incorrect-answer"); // Mauvaise rÃ©ponse
        }

        if (selectedChoices.includes(index)) {
            answerText.classList.add("selected-answer"); // RÃ©ponse sÃ©lectionnÃ©e
        }

        choicesEl.appendChild(answerText);
    });

    validateButton.classList.add("hidden");
    nextButton.classList.remove("hidden");
}

// Passer Ã  la question suivante
function nextQuestion() {
    currentQuestionIndex++;

    if (currentQuestionIndex < questions.length) {
        showQuestion();
    } else {
        endQuiz();
    }
}

// Terminer le quiz
function endQuiz() {
    quizScreen.classList.add("hidden");
    endScreen.classList.remove("hidden");
    scoreEl.textContent = `Votre score est de ${score.toFixed(2)} / ${questions.length}`;
}

// RedÃ©marrer le quiz
function restartQuiz() {
    endScreen.classList.add("hidden"); // Cache l'Ã©cran de fin
    startQuiz(); // RÃ©initialise et redÃ©marre le quiz
}